package campus;

public class Parametres {
        private int entierMini;

        private int entierMaxi;

    public Parametres(int entierMini, int entierMaxi) {
        this.entierMini = entierMini;
        this.entierMaxi = entierMaxi;
    }

    public int getEntierMini() {
        return entierMini;
    }

    public void setEntierMini(int entierMini) {
        this.entierMini = entierMini;
    }

    public int getEntierMaxi() {
        return entierMaxi;
    }

    public void setEntierMaxi(int entierMaxi) {
        this.entierMaxi = entierMaxi;
    }
}



